# [140] 单词拆分 II
# 给定一个非空字符串 s 和一个包含非空单词列表的字典 wordDict，在字符串中增加空格来构建一个句子，
# 使得句子中所有的单词都在词典中。返回所有这些可能的句子。
# 说明：
# 分隔时可以重复使用字典中的单词。
# 你可以假设字典中没有重复的单词。
# 示例 1：
# 输入:
# s = "catsanddog"
# wordDict = ["cat", "cats", "and", "sand", "dog"]
# 输出:
# [
#     "cats and dog",
#     "cat sand dog"
# ]

# 示例 2：
# 输入:
# s = "pineapplepenapple"
# wordDict = ["apple", "pen", "applepen", "pine", "pineapple"]
# 输出:
# [
#     "pine apple pen apple",
#     "pineapple pen apple",
#     "pine applepen apple"
# ]
# 解释: 注意你可以重复使用字典中的单词。

# 示例 3：
# 输入:
# s = "catsandog"
# wordDict = ["cats", "dog", "sand", "and", "cat"]
# 输出:
# []

from typing import List
# @lc code=start
class Solution:
    def wordBreak(self, s: str, wordDict: List[str]) -> List[str]:
        tmp = set("".join(wordDict))
        # 判断字符串中是否有字典中不包含的字符
        if any([i not in tmp for i in s]):
            return []
            
        w_cnt = len(wordDict)
        s_len = len(s)
        dp = [[""]]  # dp[i] 代表s的前i个字符划分成单词的所有方式
        # dp[0] = [""]   起始值
        for i in range(1, s_len + 1):
            # 注意这里的列表推导式用法
            dp.append([split + (" " if split else "") +
                       s[j:i] for j in range(i) if (s[j:i] in wordDict and dp[j]) for split in dp[j]])
                    
        return dp[s_len]

